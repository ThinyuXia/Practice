package com.xiaxinyu.ajax;

public class Song {
	private String name;
	private String type;

	public Song() {}
	
	public Song(String type, String name) {
		super();
		this.name = name;
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	
	
}
